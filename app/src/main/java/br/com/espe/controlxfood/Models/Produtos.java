package br.com.espe.controlxfood.Models;

public class Produtos {
    int id;
    int grupo;
    String nome;
    double preco;

    public Produtos(int id, int grupo, String nome, double preco) {
        this.id = id;
        this.grupo = grupo;
        this.nome = nome;
        this.preco = preco;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getGrupo() {
        return grupo;
    }

    public void setGrupo(int grupo) {
        this.grupo = grupo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getPreco() {
        return preco;
    }

    public void setPreco(double preco) {
        this.preco = preco;
    }
}
